DECLARE

W_ID_NIVEL NUMBER (10);

BEGIN
   FOR C IN (SELECT DISTINCT (TRIM(PROVINCIA)) PROVINCIA FROM JAIIO) LOOP
     
      Select ID_NIVEL INTO W_ID_NIVEL FROM NIVEL WHERE NIVEL_CODIGO = 'PROV';
      
      INSERT INTO UBICACION (ID_UBICACION,RELA_PADRE,RELA_NIVEL,CODIGO_POSTAL,DESCRIPCION)
            VALUES (-1,NULL,W_ID_NIVEL,NULL,(C.PROVINCIA));
  END LOOP;
END;